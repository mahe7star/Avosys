                           <h1>Working capital</h1>
                           <p>Working capital is the amount of an entity's current assets minus its current liabilities. The result is considered a prime measure of the short-term liquidity of an organization. A strongly positive working capital balance indicates robust financial strength, while negative working capital is considered an indicator of impending bankruptcy.<br>A 2:1 ratio of current assets to current liabilities is considered healthy, though the ratio can vary by industry. The ratio may also be reviewed on a trend line, with the intent of spotting any declines or sudden drops that could indicate liquidity problems.<br><center><strong style="font-size:15px">Working Capital = CURRENT ASSETS - CURRENT LIABILITIES</strong></center></p>
                       </div>
                   </div>
                   <div class="col-md-12">
                       <div class="f-h1-head f-h2-head">
                           <h2>Positive vs negative working capital</h2>
                           <p>Having positive working capital can be a good sign of the short-term financial health for a company because it has enough liquid assets remaining to pay off short-term bills and to internally finance the growth of their business. Without additional working capital, a company may have to borrow additional funds from a bank or turn to investment bankers to raise more money. Negative working capital means assets aren’t being used effectively, and a company may a liquidity crisis. Even if a company has lots invested in fixed assets, it will face financial challenges if liabilities come due too soon. This will lead to more borrowing, late payments to creditors and suppliers and, as a result, a lower corporate credit rating for the company.</p>
                       </div>
                   </div> 
                   <div class="col-md-12">
                       <div class="f-h1-head f-h3-head">
                           <h3>When negative working capital is ok</h3>
                           <p>Depending on the type of business, companies can have negative working capital and still do well. Examples are grocery stores like Walmart or fast-food chains like McDonald’s that can generate cash very quickly due to high inventory turnover rates and by receiving payment from customers in a matter of a few days. These companies need little working capital.<br>Products that are bought from suppliers are immediately sold to customers before the company even gets a chance to pays the vendor or supplier. In contrast, capital-intensive companies that manufacture heavy equipment and machinery usually can’t raise cash quickly, as they sell their products on a long-term payment basis. Since they can’t sell fast enough, cash won’t be available immediately during tough financial times, so having enough working capital is desirable.</p>
