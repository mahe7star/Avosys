                           <h1>What is a 'Term Loan'</h1>
                           <p>A term loan is a loan from a bank for a specific amount that has a specified repayment schedule and a fixed or floating interest rate. For example, many banks have term-loan programs that can offer small businesses the cash they need to operate from month to month. Often, a small business uses the cash from a term loan to purchase fixed assets such as equipment for its production process.<br>A term loan is for equipment, real estate or working capital paid off between one and 25 years. The loan carries a fixed or variable interest rate, monthly or quarterly repayment schedule, and set maturity date. The loan requires collateral and a rigorous approval process to reduce the risk of repayment. A term loan is appropriate for an established small business with sound financial statements and a substantial down payment to minimize payment amounts and total loan cost.</p>
                       </div>
                   </div>
                   <div class="col-md-12">
                       <div class="f-h1-head f-h2-head">
                          <h3>Features of Term Loan</h3>
                          <ul class="features-ul">
                            <li><p>Term loans are Secured Loans. The asset that is purchased using the term loan amount, will serve as a primary security and other assets of the company will be serving as collateral security.</p></li>
                            <li><p>The loan has to be repaid within the fixed term regardless of the firm’s financial situation.</p></li>
                            <li><p>The interest rate on the loan is charged after evaluating the credit risk of the proposal, the loan amount and tenure for which the loan is taken. The interest rate will be subject to a minimum lending rate. The rate is negotiated between borrowers and lenders at the time of distributing the loan.</p></li>  
                            <li><p>The term loan’s maturity lies between 5 -10 years. The repayment of the loan is made in instalments. The tenure can be rescheduled to help borrowers deal with the financial emergencies.</p></li>
                            <li><p>The lender will ask the borrower not to raise additional loans and to repay the existing loans and maintain a minimum asset base.</p></li>
                            <li><p>Term loans can be converted into equity according to the terms and conditions that have been laid out by the lender.</p></li>
                          </ul>
                       </div>
                   </div> 
                   <div class="col-md-12">
                       <div class="f-h1-head f-h2-head">
                           <h3>Advantages of Term Loans</h3>
                           <ul class="features-ul">
                            <li><p>The loan is cheap for the borrower.</p></li>
                            <li><p>The interest that the borrower pays on the term loan is tax deductible and hence can avail tax benefit on the interest paid.</p></li>
                            <li><p>The term loans are negotiable and hence the terms and conditions of the loan are not rigid.</p></li>  
                            <li><p>The term loans represent debt financing and the interest of the equity shareholder is not weakened.</p></li>
                            <li><p>The lender will have a collateral security and hence the loan is not a huge risk to the financial institution.</p></li>
                            <li><p>Since the loan can be converted to equity, the lender can get the right to control the affairs of the business or firm.</p></li>
                          </ul>
                   </div>   
                   <div class="col-md-12">
                       <div class="f-h1-head f-h2-head">
                           <h3>Disadvantages of Term Loans</h3>
                           <ul class="features-ul">
                            <li><p>If the borrower fails to make the repayments, the lender will question the borrower’s liquidity position and the company’s existence will be at stake.</p></li>
                            <li><p>Debt financing increases the financial risk of the company. It adversely affects the benefits of the shareholders.</p></li>
                            <li><p>In addition to the collateral security, the borrower will have to tend to the restrictive covenants imposed by the lenders. The borrower will have to close the existing loans and must maintain the asset base and not take another loans. This causes unnecessary interference in the firm’s functioning.</p></li>  
                            <li><p>Since the terms and conditions are negotiable, there is chance that it might affect the interest of the lender.</p></li>
                            <li><p>The lender of term loan will have no control over the company’s affairs and it leads to the lender asking the borrower to convert the loan to equity.</p></li>
                          </ul>
                       </div>
                   </div> 
                   <div class="col-md-12">
                       <div class="f-h1-head f-h2-head">
                           <h3 style="margin-bottom: 15px">Types of Term Loan</h3>
                           <h4 style="text-decoration: underline;">Classified As Per Facility</h4>
                           <ul class="features-ul">
                             <li><p><strong>Line-Of-Credit: </strong> This type of loan is generally availed by the small business owners. The line of credit loan helps businesses to meet the working capital requirements like the purchase of inventory, daily expenses, etc. These loans carry a low rate of interest. Every business must make this type of loan arrangement with banks so that they always have funds to meet their routine expenses. Interest payment for this type of term loan is on monthly basis. However, the principal payment can be made as per the suitability. But it is wiser to make timely principal payments.</p></li>
                             <li><p><strong>Letter of Credit: </strong> Letter of credit facility is given to businesses indulging in international transactions. Letter of credit ensures payment to the supplier in another country. In the case of non-payment by the buyer to the bank in the home country, the bank holds the right to seize buyer’s asset and sell it in open market to recover the amount. Though it is not a direct form of term loan yet it acts like a loan to business organisations.</p></li>
                           </ul><br>
                           <h4 style="text-decoration: underline;">Classified As Per Duration/Tenor</h4>
                           <ul class="features-ul">
                             <li><p><strong>Long-Term Loan: </strong> As the name suggests, the long-term loan is for a longer duration of time. This type of loan is suitable for those businesses that are expanding the business, acquiring another business, refinancing, etc. A business having goodwill and a better track record in relation to the repayment of loans can easily obtain a long-term loan. The loan instalments are on monthly basis. The rate of interest is lower than the short-term loans.</p></li>
                             <li><p><strong>Short-Term Loan: </strong> Short- term loans are for a period of less than one year. This type of loan is useful for those businesses which are seasonal in nature where the main business period is confined to the limited period of time in a year. This loan is utilised by the business to meet its working capital requirements. The rate of interest is generally higher. The loan payment is to be made in full at the end of agreed term rather than paying in form of monthly instalments.</p></li>
                             <li><p><strong> Interim Loan: </strong> Interim loan is useful for businesses that run out of cash and need to make urgent payments. Banks grant loan only to those borrowers who are reliable and timely pay off the loan amount. The bank keeps assets like building, machinery, etc. as a mortgage while granting the interim loan. This short-term business financing arrangement is best if you wish to buy commercial property or during construction of the commercial property.</p></li>
                           </ul><br>
                           <h4 style="text-decoration: underline;">Classified As Per Security</h4>
                           <ul class="features-ul">
                             <li><p><strong>Secured and Unsecured Loan: </strong> Interim loan is useful for businesses that run out of cash and need to make urgent payments. Banks grant loan only to those borrowers who are reliable and timely pay off the loan amount. The bank keeps assets like building, machinery, etc. as a mortgage while granting the interim loan. This short-term business financing arrangement is best if you wish to buy commercial property or during construction of the commercial propertySecured loans are those loans on which the banks demand a collateral security in exchange. The collateral security can be any asset of the business. The bank has the right to sell off the asset and recover the money in case of non-payment of the loan amount. The period of secured loans is more than 12 months. The rate of interest is comparatively lower than the unsecured loans. On the other hand, unsecured loans do not have any collateral security in exchange for a loan. Banks grant unsecured loans only when they are sure of the repaying ability of the borrower.</p></li>
                           </ul>
